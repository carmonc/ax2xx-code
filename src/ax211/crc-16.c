uint32_t crc32(const void *buf, int size)
{
    const uint8_t *p;
    uint32_t crc = 0;

    p = buf;
    crc = crc ^ ~0U;

    while (size--)
        crc = crc32_tab[(crc ^ *p++) & 0xFF] ^ (crc >> 8);

    return crc ^ ~0U;
}
